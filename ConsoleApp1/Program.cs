// Объявляем двумерный массив вещественных чисел размером 3x3
double[,] matrix = new double[3, 3] { { 1.2, 2.4, 3.6 }, { 4.8, 5.1, 6.3 }, { 7.5, 8.2, 9.7 } };

// Выводим исходный массив на консоль
Console.WriteLine("Исходный массив:");
for (int i = 0; i < 3; i++)
{
    for (int j = 0; j < 3; j++)
    {
        Console.Write(matrix[i, j] + " ");
    }
    Console.WriteLine();
}

// Проходим циклом по всем элементам массива и меняем местами строки и столбцы с одинаковыми номерами
for (int i = 0; i < 3; i++)
{
    for (int j = i; j < 3; j++)
    {
        if (i == j) // Если номер строки и столбца совпадают
            continue;
        // Меняем местами строку и столбец
        double temp = matrix[i, j];
        matrix[i, j] = matrix[j, i];
        matrix[j, i] = temp;
    }
}

// Выводим измененный массив на консоль
Console.WriteLine("Измененный массив:");
for (int i = 0; i < 3; i++)
{
    for (int j = 0; j < 3; j++)
    {
        Console.Write(matrix[i, j] + " ");
    }
    Console.WriteLine();
}

Console.ForegroundColor = ConsoleColor.Black;

// Объяснение кода

// Массив состоит из 3 строк и 3 столбцов, то есть 9 элементов в общей сложности. 
// С помощью двух циклов мы перебираем каждый элемент массива.
// Для изменения местами строк и столбцов с одинаковыми номерами используется дополнительная переменная temp, 
// которая позволяет сохранить значение на какой-то момент времени, то есть обменять местами значения.
// В результате получаем измененный массив, где строки и столбцы с одинаковыми номерами поменялись местами.